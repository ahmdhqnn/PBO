package pbo.assesment.assement1;


public class TempatWisata {
    private String nama;
    private int hargaTiket;

    public TempatWisata(String nama, int hargaTiket) {
        this.nama = nama;
        this.hargaTiket = hargaTiket;
    }

    public String getNama() {
        return nama;
    }

    public int getHargaTiket() {
        return hargaTiket;
    }
}
